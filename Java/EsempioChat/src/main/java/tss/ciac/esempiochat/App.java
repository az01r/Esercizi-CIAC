    /**
     * aggiorna l'oggetto grafico jListUsers
     */
    private void refreshListUsers() {
        DefaultListModel listModel = new DefaultListModel();
        for (User u : listUsers) {
            //aggiungi String alla lista
            listModel.addElement(u.getUserName());
        }
        jListUsers.setModel(listModel); // scrive nell'oggetto grafico jListUsers
    }

    /**
     * aggiorna l'oggetto grafico jTextPaneMessaggiInviati
     */
    private void refreshChat() {
        String chat = "";
        for (int i = listMessages.size() - 1; i >= 0; i--) {
            chat += "from: " + listMessages.get(i).getFromUser() + "\n";
            chat += "to: " + listMessages.get(i).getToUser() + "\n";
            chat += "date: " + listMessages.get(i).getDataInvio() + "\n";
            chat += "msg: " + listMessages.get(i).getMessageUser() + "\n";
            chat += "\n-------------------------------------------------\n\n";
        }
        jTextPaneMessaggiInviati.setText(chat); // scrive nell'oggetto grafico jTextPaneMessaggiInviati
    }

