    private void jButtonLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLoginActionPerformed
        // TODO add your handling code here:
        String userName = jTextFieldUserName.getText();
        String password = jTextFieldPassword.getText();
        int trovato = -1; // -1 indica non trovato, se lo trovo gli assegno la posizione tramite i
        for(int i = 0;i<listUsers.size();i++){
            if(listUsers.get(i).getUserName().equals(userName) && listUsers.get(i).getPassword().equals(password)){ // NB occhio a non usare == per confrontare Stringhe perché sono oggetti
                trovato = i;
                break;
            }
        }
        if (trovato != -1) {
            loggedUser = trovato; // passo l'indice in listUsers
            jLabelOut.setText("Logged as " + listUsers.get(loggedUser).getUserName());
            jButtonLogin.setEnabled(false); // disabilito il tasto login
            /*
                --------------------------------------------
                sarebbe meglio modificare il tasto in LOGOUT
                --------------------------------------------
             */
            jButtonSendMessage.setEnabled(true); // abilito il tasto sendMessage
        } else {
            jLabelOut.setText("Wrong username or password");
        }
    }//GEN-LAST:event_jButtonLoginActionPerformed

    private void jButtonSendMessageActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSendMessageActionPerformed
        // TODO add your handling code here:
        Message msg = new Message(listUsers.get(loggedUser).getUserName(), listUsers.get(jListUsers.getSelectedIndex()).getUserName(), jTextAreaMessageIn.getText());
        // fromUser --> listUsers.get(loggedUser).getUserName()
        // toUser --> listUsers.get(jListUsers.getSelectedIndex()).getUserName()
        // message --> jTextAreaMessageIn
 //     Message msg = new Message(listUsers.get(loggedUser).getUserName(), "destinatario", jTextAreaMessageIn.getText());
        listMessages.add(msg); // aggiungo il messaggio a listMessages
        refreshChat();
    }//GEN-LAST:event_jButtonSendMessageActionPerformed

