package interfacce2;

/**
 *
 * @author alfonso
 */
public class Persona implements Loggable{
    
    private String nome;
    private int eta;

    public Persona(String nome, int eta) {
        this.nome = nome;
        this.eta = eta;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getEta() {
        return eta;
    }

    public void setEta(int eta) {
        this.eta = eta;
    }

    @Override
    public void log() {
        System.out.println("nome: " + nome + ", eta: " + eta);
    }
    
    
}
