    private void btnScriviMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnScriviMouseClicked
        // TODO add your handling code here:
        String tx = txInputNome.getText() + " " + txInputCognome.getText();
        txOutput.setText(tx);
        String t = tpOutput.getText() + tx + "\n";
        tpOutput.setText(t);
        float costo = Float.parseFloat(txInputCosto.getText());
        consulenteCorrente = new Consulente(txInputNome.getText(), txInputCognome.getText(), costo);
        elConsulenti.add(consulenteCorrente);
        tpOutputElConsulenti.setText(getElencoConsulenti()); //aggiorna tp
        lblInfo.setText(consulenteCorrente.infoHtml());
        refreshList(); //aggiorna lista
    }//GEN-LAST:event_btnScriviMouseClicked

