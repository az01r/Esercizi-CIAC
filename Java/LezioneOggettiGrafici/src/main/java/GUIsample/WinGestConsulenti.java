    public void refreshList() {
        DefaultListModel listModel = new DefaultListModel();
        int i = 1;
        for (Consulente c : elConsulenti) {
            String item = i + ") " + c.getNome() + " " + c.getCognome();
            listModel.addElement(item);
            i++;
        }
        lsConsulenti.setModel(listModel);
    }

