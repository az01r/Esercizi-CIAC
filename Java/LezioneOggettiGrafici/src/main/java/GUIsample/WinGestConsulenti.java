    public String getElencoConsulenti() {
        String tx = "";
        for (int i = 0; i < elConsulenti.size(); i++) {
            tx += (i + 1) + ") " + elConsulenti.get(i).getNome() + " " + elConsulenti.get(i).getCognome() + " " + elConsulenti.get(i).getCosto() + "\n";
        }
        return tx;
    }

