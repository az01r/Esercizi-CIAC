    private void jButtonModaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonModaActionPerformed
        // TODO add your handling code here:
        int moda=0;
        int occorrenze = 0, occorrenzeModa = 0;
        ArrayList<Integer> numeriMod = (ArrayList<Integer>) numeri.clone(); // copia dell'ArrayList numeri senza i valori gi√† controllati

        while (0 < numeriMod.size()) {
            occorrenze = 0;
            for (int j = 0; j < numeriMod.size(); j++) {
                if (numeriMod.get(j) == numeriMod.get(0)) {
                    occorrenze++;
                    if (occorrenze > occorrenzeModa) {
                        occorrenzeModa = occorrenze;
                        moda = numeriMod.get(j); 
                    }
                    numeriMod.remove(j); // tolgo il valore appena controllato
                }
            }

        }
        jLabelModa.setText(""+moda);
    }//GEN-LAST:event_jButtonModaActionPerformed

    private void jButtonOrdinaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonOrdinaActionPerformed
        // TODO add your handling code here:
        //scorro un numero per volta
            for (int i = 1; i < numeri.size(); i++) {
                boolean scambio = false;
                for (int j = 1; j < numeri.size(); j++) {
                    if (numeri.get(j - 1) > numeri.get(j)) {
                        //scambio
                        int tmp = numeri.get(j - 1);
                        numeri.set(j - 1, numeri.get(j));
                        numeri.set(j, tmp);
                        scambio = true;
                    }
                    //se nessuno scambio esco

                }
                if (scambio == false) {
                    break;
                }
            }
            refreshList();
    }//GEN-LAST:event_jButtonOrdinaActionPerformed

